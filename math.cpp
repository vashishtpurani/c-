#include<iostream>
#include<cmath>
using namespace std;
int main(){

    short  s = -30;
    int    i = 20;
    long   l = 1000000;
    float  f = -123.432;
    double d = 2312.1231;

    cout << "short  s :" << s << endl;
    cout << "int    i :" << i << endl;
    cout << "long   l :" << l << endl;
    cout << "float  f :" << f << endl;
    cout << "double d :" << d << endl;

    cout<<"---------------------------------------------"<<endl;

    cout << "sin(i) = "<< sin(i) << endl;
    cout << "abs(s) = "<< abs(s) << endl;
    cout << "floor(f) = "<< floor(f) << endl;
    cout << "sqrt(i) = "<< sqrt(l) << endl;
    cout << "pow(i) = "<< pow(i,2) << endl;


}