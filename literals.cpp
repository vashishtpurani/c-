#include<iostream>
using namespace std;
int main(){
    cout<<"\\ "<<endl;/* \ */
    cout<<"\' "<<endl;//'
    cout<<"\" "<<endl;//"
    cout<<"\? "<<endl;//?
    cout<<"\a "<<endl;//alert sound
    cout<<" hello\b "<<endl;//backspace
    cout<<"\fgoodbye "<<endl;// :|
    cout<<"hello\r "<<endl;//goes to start of line and deletes 
    cout<<"\v \v \vgoodbye "<<endl;// vertical tab
    
}